// Arithematic Operators
console.log("Arithematic Operators");
let a = 20, b = 10;
console.log( a + " + " + b  + " = " + (a+b));     //20 + 10 = 30
console.log( a + " - " + b  + " = " + (a-b));     //20 - 10 = 10
console.log( a + " * " + b  + " = " + (a*b));     //20 * 10 = 200
console.log( a + " / " + b  + " = " + (a/b));     //20 / 10 = 2
console.log( a + " % " + b  + " = " + (a%b));     //20 % 10 = 0


// Assignment Operators
console.log("Assignment Operators");
let aso = 10;  //10
console.log("a = " + aso); //10

aso += 5  //10 + 5
console.log("a += " + aso);  //15

aso -= 5  //10 + 5
console.log("a -= " + aso);  //15

aso *= 5  //10 + 5
console.log("a *= " + aso);  //15

aso /= 5  //10 + 5
console.log("a /= " + aso);  //15

aso %= 4  //10 + 5
console.log("a %= " + aso);  //15